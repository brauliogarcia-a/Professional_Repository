            <div class="texto1"><br><br><strong>¿Cuáles son los métodos de envío?</strong><br><br>

Los envíos se hacen a través de compañías especializadas en grupaje de mobiliario, realizando el servicio puerta a puerta, es decir, recogen a puerta de nuestro almacén y entregan a puerta de su edificio, no realizando el servicio de subida a pisos, ni servicio de desembalaje, montaje u otros, salvo contratación expresa y disponible.<br><br>

<strong>¿Existen gastos de envío?</strong><br><br>

Los gastos ocasionados por el envío del pedido correrán a cargo del comprador siendo indicados previamente a la formalización del pedido y calculados según volumen y destino. Para otras regiones del mundo consultar en el departamento de atención al cliente.<br><br>

<strong>¿Cuál es el periodo de entrega del pedido?</strong><br><br>

El periodo aproximado de entrega de la mercancía aparecerá especificado en cada artículo y en la cesta de la compra una vez seleccionado el artículo.
